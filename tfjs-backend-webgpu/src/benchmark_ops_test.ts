function compareAddFloat32Array(
    result: any, firstMatrix: any, secondMatrix: any, w: number, h: number) {
  for (let i = 0; i < w * h; i++) {
    if (Math.abs(result[i] - (firstMatrix[i] + secondMatrix[i])) > 0.01) {
      console.error(name + ' mismatch at ' + i);
      return i + 1;
    }
  }
  return 0;
}

